#Import necessary libraries
import pandas as pd
import yfinance as yf
from sklearn.preprocessing import MinMaxScaler
import numpy as np
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Reshape, Dropout
from sklearn.metrics import mean_squared_error
import datetime

# Define the stocks and their tickers
stocks = {
    'Bank Nifty': '^NSEBANK',
    'HDFC Bank Ltd': 'HDFCBANK.NS',
    'ICICI Bank Ltd': 'ICICIBANK.NS',
    'Axis Bank Ltd': 'AXISBANK.NS',
    'State Bank of India': 'SBIN.NS',
    'Kotak Mahindra Bank Ltd': 'KOTAKBANK.NS',
    'Indusind Bank Ltd': 'INDUSINDBK.NS',
    'Bank of Baroda Ltd': 'BANKBARODA.NS',
    'Federal Bank Ltd': 'FEDERALBNK.NS',
    'Punjab National Bank': 'PNB.NS',
    'IDFC First Bank Ltd': 'IDFCFIRSTB.NS',
    'AU Small Finance Bank Ltd': 'AUBANK.NS',
    'Bandhan Bank Ltd': 'BANDHANBNK.NS'
}

# Initialize a DataFrame to store daily data for all stocks
all_data = pd.DataFrame()

# Fetch data for each stock and merge into one DataFrame
for stock, ticker in stocks.items():
    data = yf.download(ticker, start='2019-01-01', end='2024-04-01')
    all_data[stock + '_Close'] = data['Adj Close']

# Print the DataFrame
#print(all_data)

"""**Data Preprocessing**"""

# Drop rows with any missing values (considering interpolation for better handling)
all_data.interpolate(method='linear', inplace=True)
all_data.dropna(inplace=True)

# Function to smooth outliers based on IQR (Interquartile Range) method
def smooth_outliers(df):
    for column in df.columns:
        Q1 = df[column].quantile(0.25)
        Q3 = df[column].quantile(0.75)
        IQR = Q3 - Q1
        lower_bound = Q1 - 1.5 * IQR
        upper_bound = Q3 + 1.5 * IQR

        # Replace outliers with median
        median = df[column].median()
        df[column] = df[column].apply(lambda x: median if x < lower_bound or x > upper_bound else x)
    return df

# Smooth outliers in the data
all_data_smoothed = smooth_outliers(all_data)

# Normalize the data using MinMaxScaler
scaler = MinMaxScaler()
all_data_scaled = pd.DataFrame(scaler.fit_transform(all_data_smoothed), index=all_data.index, columns=all_data.columns)

all_data_scaled.head()

"""**Visualize the historical closing prices of Bank Nifty and its Constituents**"""

import matplotlib.pyplot as plt
# Set up the subplots
num_stocks = len(stocks)
cols = 4
