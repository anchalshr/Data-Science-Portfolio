"""**Data Preparation for LSTM Modeling:**

*  **Create 60-day historical sequences for 5-day price prediction**
*  **Split data into 80% training, 20% testing sets**
"""

# Function to create sequences for LSTM
def createSequences(data, sequenceWindow, weekWindow):
    X, y = [], []
    for i in range(len(data) - sequenceWindow - weekWindow):
        X.append(data[i:(i + sequenceWindow)])
        y.append(data[(i + sequenceWindow):(i + sequenceWindow + weekWindow)])
    return np.array(X), np.array(y)

# Choose sequence length - 60 days = 12 weeks
sequenceWindow = 60
weekWindow = 5
X, y = createSequences(all_data_scaled.values, sequenceWindow, weekWindow)

# Split data into training and testing sets - 80:20 split
train_size = int(0.8 * len(X))
X_train, X_test = X[:train_size], X[train_size:]
y_train, y_test = y[:train_size], y[train_size:]

"""**LSTM Model Architecture and Training**"""

# Build LSTM model
model = Sequential()
model.add(LSTM(units=50, return_sequences=True, input_shape=(sequenceWindow, all_data_scaled.shape[1])))
model.add(Dropout(0.2))
model.add(LSTM(units=100))
model.add(Dropout(0.2))
model.add(Dense(5 * 13))  # 5 days * 13 stocks
model.add(Reshape((5, 13)))  # Reshape to match the target shape

# Compile the model
model.compile(optimizer='adam', loss='mean_squared_error')

# Train the model
history= model.fit(X_train, y_train, epochs=200, batch_size=100, validation_split=0.2)

# Check history for learning curves
print(history.history.keys())

import matplotlib.pyplot as plt

# Plot training and validation loss
plt.figure(figsize=(10, 6))
plt.plot(history.history['loss'], label='Training Loss')
plt.plot(history.history['val_loss'], label='Validation Loss')
plt.title('LSTM model performance_phase1')
plt.xlabel('Epochs')
plt.ylabel('Loss')
plt.legend()
plt.grid(True)
plt.show()

#Disply a summary of the model's architecture
model.summary()

"""**Model Evaluation and Performance Metrics:**

*  **Generate predictions and inverse transform to original scale**
*  **Calculate MSE and RMSE for overall model performance**
*  **Compute specific RMSE for Bank Nifty index**
"""

# Make predictions, reshaping to match y_test
y_pred_scaled = model.predict(X_test).reshape(y_test.shape)

# Inverse transform the predictions
y_pred = scaler.inverse_transform(y_pred_scaled.reshape(-1, all_data_scaled.shape[1])).reshape(y_test.shape)

# Calculate MSE for each stock and each day
mse_per_stock_day = np.zeros((y_test.shape[1], y_test.shape[2]))
for stock_idx in range(y_test.shape[1]):
    for day_idx in range(y_test.shape[2]):
        mse_per_stock_day[stock_idx, day_idx] = mean_squared_error(
            y_test[:, stock_idx, day_idx], y_pred[:, stock_idx, day_idx]
        )

# Calculate overall MSE
overall_mse = np.mean(mse_per_stock_day)

# Calculate RMSE
overall_rmse = np.sqrt(overall_mse)

print("Overall MSE:", overall_mse)
print("Overall RMSE:", overall_rmse)

# Calculate RMSE for Bank Nifty - first column
bank_nifty_mse = np.mean(mse_per_stock_day[0, :])  # Average over days
bank_nifty_rmse = np.sqrt(bank_nifty_mse)

print("Bank Nifty RMSE:", bank_nifty_rmse)

"""**Model Prediction**"""

# Prepare the most recent data for prediction
latest_data = all_data_scaled.values[-sequenceWindow:]
X_pred = latest_data.reshape(1, sequenceWindow, all_data_scaled.shape[1])

# Use the trained model to make predictions
y_pred_scaled = model.predict(X_pred)

# Inverse transform the predictions to get actual values
y_pred = scaler.inverse_transform(y_pred_scaled.reshape(-1, all_data_scaled.shape[1]))

# Extract and format the predictions
last_date = all_data.index[-1]
prediction_dates = pd.date_range(start=last_date + pd.Timedelta(days=1), periods=5)
predictions = pd.DataFrame(y_pred, columns=all_data.columns, index=prediction_dates)

# Display the predictions
print("Predictions for the next 5 days:")
print(predictions)

