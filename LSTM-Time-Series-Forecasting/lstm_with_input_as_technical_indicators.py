all_technical_data.shape

"""**Predicting the next day's Bank Nifty closing price (regression)**"""

# Shift the target column to create labels for the next day (one-step ahead forecasting)
all_technical_data['Target'] = all_technical_data['Bank Nifty_Bank Nifty_Close'].shift(-1)

# Drop the last row (since it won't have a valid target value)
all_technical_data.dropna(inplace=True)

from sklearn.preprocessing import MinMaxScaler

# Select the features and target
features = all_technical_data.drop(columns=['Target'])  # All columns except the target
target = all_technical_data['Target']  # Target is next day's close price

# Initialize scalers
scaler_features = MinMaxScaler(feature_range=(0, 1))
scaler_target = MinMaxScaler(feature_range=(0, 1))

# Scale features and target
features_scaled = scaler_features.fit_transform(features)
target_scaled = scaler_target.fit_transform(target.values.reshape(-1, 1))

"""**Data Preparation for LSTM Modeling**

**Create 60-day historical sequences for next day price prediction**

**Split data into 80% training, 20% testing sets**
"""

import numpy as np

# Define sequence length
sequence_length = 60

def create_sequences(data, target, seq_length):
    X, y = [], []
    for i in range(len(data) - seq_length):
        X.append(data[i:i + seq_length])  # Sequence of `seq_length` days
        y.append(target[i + seq_length])  # The target is the value at `i + seq_length`
    return np.array(X), np.array(y)

# Create sequences
X, y = create_sequences(features_scaled, target_scaled, sequence_length)

from sklearn.model_selection import train_test_split

# Split data into training and testing sets (e.g., 80% training, 20% testing)
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2, shuffle=False)

# Create sequences
