# Initialize a DataFrame to store daily data for all stocks
all_data = pd.DataFrame()

# Fetch data for each stock and merge into one DataFrame
for stock, ticker in stocks.items():
    data = yf.download(ticker, start='2019-01-01', end='2024-04-01')
    all_data[stock + '_Close'] = data['Adj Close']

# Print the DataFrame
#print(all_data)

def add_technical_indicators(df, column_name):
# Create a working DataFrame just for this stock
    df_stock = pd.DataFrame(df[column_name])

    # Simple Moving Averages
    df_stock['SMA_20'] = df_stock[column_name].rolling(window=20).mean()
    df_stock['SMA_50'] = df_stock[column_name].rolling(window=50).mean()
    df_stock['SMA_200'] = df_stock[column_name].rolling(window=200).mean()

    # Exponential Moving Averages
    df_stock['EMA_20'] = df_stock[column_name].ewm(span=20, adjust=False).mean()
    df_stock['EMA_50'] = df_stock[column_name].ewm(span=50, adjust=False).mean()

    # RSI (Relative Strength Index)
    df_stock['RSI'] = ta.momentum.RSIIndicator(close=df_stock[column_name], window=14).rsi()

    # MACD (Moving Average Convergence Divergence)
    macd = ta.trend.MACD(close=df_stock[column_name])
    df_stock['MACD'] = macd.macd()
    df_stock['MACD_Signal'] = macd.macd_signal()
    df_stock['MACD_Hist'] = macd.macd_diff()

    return df_stock

# Initialize a dictionary to store the data for all stocks with their respective indicators
all_technical_data = pd.DataFrame()

# Iterate over each stock and compute technical indicators
for stock, ticker in stocks.items():
    column_name = stock + '_Close'
    stock_indicators = add_technical_indicators(all_data, column_name)

    # Add a prefix to each column to identify which stock the indicators belong to
    stock_indicators = stock_indicators.add_prefix(stock + '_')

    # Merge the stock's technical indicators into the main DataFrame
    if all_technical_data.empty:
        all_technical_data = stock_indicators
    else:
        all_technical_data = pd.concat([all_technical_data, stock_indicators], axis=1)

# Display the combined DataFrame with technical indicators
#print(all_technical_data)

# Step 1: Interpolate missing values linearly
all_technical_data.interpolate(method='linear', inplace=True)

# Step 2: Drop any remaining NaN values
all_technical_data.dropna(inplace=True)

