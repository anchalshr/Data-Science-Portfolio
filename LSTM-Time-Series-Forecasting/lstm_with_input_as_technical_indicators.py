X_train.shape

X_test.shape

"""**LSTM Model Architecture and Training**"""

import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout

# Build LSTM Model
model = Sequential()

# LSTM layer 1 (return_sequences=True for passing sequences to the next LSTM layer)
model.add(LSTM(units=100, return_sequences=True, input_shape=(sequence_length, X_train.shape[2])))
model.add(Dropout(0.5))  # Dropout for regularization

# LSTM layer 2 (final LSTM layer, no need for return_sequences)
model.add((LSTM(100, return_sequences=False)))
model.add(Dropout(0.5))

# Output layer for regression (1 neuron to predict next value)
model.add(Dense(1, activation='linear'))

# Compile the model (using mean squared error loss for regression)
model.compile(optimizer='adam', loss='mean_squared_error')

# Train the model with a validation split (no shuffle, as it's time-series data)
history = model.fit(X_train, y_train, validation_data=(X_test,y_test), epochs=40,batch_size=40, verbose=1, shuffle =True)

