"""**Import required libraries**"""

import seaborn as sns
from sklearn.compose import ColumnTransformer
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.preprocessing import OneHotEncoder
from sklearn.pipeline import Pipeline
from sklearn.linear_model import LogisticRegressionCV
from sklearn.model_selection import train_test_split
from sklearn.tree import DecisionTreeClassifier
from sklearn.naive_bayes import MultinomialNB
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report, ConfusionMatrixDisplay
import matplotlib.pyplot as plt
from sklearn import tree

"""**Load the dataset**"""

train=pd.read_csv('/kaggle/input/sentiment-prediction-on-movie-reviews/train.csv')
test=pd.read_csv('/kaggle/input/sentiment-prediction-on-movie-reviews/test.csv')
movies=pd.read_csv('/kaggle/input/sentiment-prediction-on-movie-reviews/movies.csv')

#summary of the dataframe
train.info()
#movies.info()

"""**Perform EDA(Exploratory data analysis)**

Merge the movies and train dataset
"""

movies = movies.drop_duplicates()
movies = movies.drop_duplicates(subset=['movieid'])
df= train.merge(movies, on='movieid', how='left')
df.shape

#summary of the dataframe (df)
df.info()

"""**Visualize the correlation matrix**"""

correlation_matrix = df.corr()
plt.figure(figsize=(8, 6))
sns.heatmap(correlation_matrix, annot=True, cmap='coolwarm', fmt=".2f")
plt.title('Correlation Matrix')
plt.show()

"""**Bar plot for Sentiment distribution**"""

# Creating a DataFrame from the data
df_sentiment = pd.DataFrame(df['sentiment'])

# Bar plot for Sentiment distribution
sns.countplot(x='sentiment', data=df_sentiment)
plt.title('Sentiment Distribution')
plt.show()

"""****

**Detecting the missing values and imputation**
"""

#df.isnull().sum()
df=df.fillna('')
#df.isnull().sum()

#consider a common feature list to be considered for the analysis
features_to_consider=['reviewText','director','originalLanguage']

# Reorder columns in the dataset based on the common feature list
X_train=df[features_to_consider]
X_train.shape

"""**Feature engineering using TfidfVectorizer and OneHotEncoder**

**Define columnTransformer named CT**
"""

CT = ColumnTransformer([
        ("review_text", TfidfVectorizer(), 'reviewText'),
    ("categorical", OneHotEncoder(handle_unknown='ignore'), ['director','originalLanguage'])
        ])

"""**Model 1: Logistic RegressionCV**

**Create a pipeline with ColumnTransformer and Logistic RegressionCV**
"""

pipeline = Pipeline([
    ("preprocessor", CT),
    ("classifier", LogisticRegressionCV())
   ])

"""**Split data into training and test sets**"""

x_train, x_test, y_train, y_test=train_test_split(X_train,df['sentiment'],test_size=0.3, random_state=42)

"""**Fit the pipeline on the training data**"""

pipeline.fit(x_train,y_train)

"""**Evaluating the model performance**"""

y_pred=pipeline.predict(x_test)

"""**Check the accuracy_score,confusion_matrix and classification_report**"""

#evaluate the model's accuracy
accuracy=accuracy_score(y_test, y_pred)
accuracy

#create a confusion matrix
cm=confusion_matrix(y_test, y_pred)
display=ConfusionMatrixDisplay(confusion_matrix=cm)
display.plot()
plt.show()

#generate a classification report
print(classification_report(y_test, y_pred))

"""**Model No.2 DecisionTreeClassifier**"""

#Create a pipeline with ColumnTransformer and DecisionTreeClassifier
pipeline1 = Pipeline([
    ("preprocessor", CT),
    ("clf",DecisionTreeClassifier(random_state=0))
   ])

#Fit the pipeline on the training data
pipeline1.fit(x_train,y_train)

"""**Visualize the decision tree**"""

clf= pipeline1.named_steps["clf"]
# Visualize the decision tree using tree.plot_tree
plt.figure(figsize=(20, 10))
tree.plot_tree(clf,max_depth=3,filled=True)
plt.show()

y_pred1=pipeline1.predict(x_test)

#evaluate the model's accuracy
accuracy1=accuracy_score(y_test, y_pred1)
accuracy1

"""**Define the model No.3 MultinomialNB**"""

#Create a pipeline with ColumnTransformer and MultibinomialNB
pipeline2 = Pipeline([
    ("preprocessor", CT),
    ("mnb",MultinomialNB())
   ])

pipeline2.fit(x_train,y_train)

y_pred2=pipeline2.predict(x_test)

accuracy2=accuracy_score(y_test, y_pred2)
accuracy2

"""**Predict the output based on the selected model, that is, model 1,Logistic RegressionCV**

**Merge the movies and test dataset**
"""

df_test=test.merge(movies, on='movieid', how='left')
df_test.shape

#consider a common feature list to be considered for the analysis
#features_to_consider=['reviewText','director','originalLanguage']
df_test=df_test[features_to_consider]
df_test

#Detecting the missing values and imputation
#df_test.isnull.sum()
df_test=df_test.fillna('')
df_test.info()

#predicting the output based on the selected model
y_test_pred=pipeline.predict(df_test)

#Submitting the csv file to the competition

submission=pd.DataFrame(columns=['id','sentiment'])
submission['id']=[i for i in range(len(y_test_pred))]
submission['sentiment']=y_test_pred
submission.to_csv('submission.csv', index=False)

